	MGRCLI VARCHAR2(100 CHAR),
	REGPLACECLI VARCHAR2(100 CHAR),
	RESULTCODE VARCHAR2(100 CHAR),
	RESULTTEXT VARCHAR2(500 CHAR),
	t_timestamp date 
