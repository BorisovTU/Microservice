   COMMENT ON COLUMN SOFR_CONTRACTBOOUT.MGRCLI IS 'Менеджер клиента';
   COMMENT ON COLUMN SOFR_CONTRACTBOOUT.REGPLACECLI IS 'Место регистрации клиента';
