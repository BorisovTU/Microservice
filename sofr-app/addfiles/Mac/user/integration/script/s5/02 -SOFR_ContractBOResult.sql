   COMMENT ON COLUMN SOFR_CONTRACTBORESULT.REGPLACECLI IS 'Место регистрации клиента';
   COMMENT ON COLUMN SOFR_CONTRACTBORESULT.RESULTCODE IS 'Код ошибки';
   COMMENT ON COLUMN SOFR_CONTRACTBORESULT.RESULTTEXT IS 'Текст ошибки';
