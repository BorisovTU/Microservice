	CONTRACTDATE DATE NOT NULL ,
	TYPETP NUMBER(10) NOT NULL ,
	FUNDS VARCHAR2(100 CHAR), 
	CLIENTTYPE NUMBER(10) NOT NULL ,
	CLIENTID VARCHAR2(10 CHAR) NOT NULL ,
	BRANCHID VARCHAR2(4 CHAR) NOT NULL ,
