-- Таблица "DNPTXLINKSKINDSNOBCH_DBT"

CREATE TABLE DNPTXLINKSKINDSNOBCH_DBT (
    T_ID NUMBER(10)
  , T_LNKID NUMBER(10)
  , T_TYPESNOB_OLD NUMBER(10)
  , T_TAXBASETYPE_OLD NUMBER(10)
  , T_BEGDATETYPESNOB_OLD DATE
  , T_ENDDATETYPESNOB_OLD DATE
  , T_TYPESNOB_NEW NUMBER(10)
  , T_TAXBASETYPE_NEW NUMBER(10)
  , T_BEGDATETYPESNOB_NEW DATE
  , T_ENDDATETYPESNOB_NEW DATE
  , T_USER NUMBER(5)
  , T_DREC DATE
)
/

