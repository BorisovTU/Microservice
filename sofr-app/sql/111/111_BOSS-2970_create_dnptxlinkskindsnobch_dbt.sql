CREATE UNIQUE INDEX DNPTXLINKSKINDSNOBCH_DBT_IDX0 ON DNPTXLINKSKINDSNOBCH_DBT (
   T_ID ASC
)
/

CREATE   INDEX DNPTXLINKSKINDSNOBCH_DBT_IDX1 ON DNPTXLINKSKINDSNOBCH_DBT (
   T_LNKID ASC
)
/

CREATE SEQUENCE DNPTXLINKSKINDSNOBCH_DBT_SEQ 
  START WITH 1
  MAXVALUE 999999999999999999999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  NOORDER
/

CREATE OR REPLACE TRIGGER DNPTXLINKSKINDSNOBCH_DBT_T0_AINC
  BEFORE INSERT OR UPDATE OF T_ID ON DNPTXLINKSKINDSNOBCH_DBT FOR EACH ROW
DECLARE
  v_id INTEGER;
BEGIN
  IF (:NEW.T_ID = 0 OR :NEW.T_ID IS NULL) THEN
    SELECT DNPTXLINKSKINDSNOBCH_DBT_SEQ.NEXTVAL INTO :NEW.T_ID FROM DUAL;
  ELSE
    SELECT LAST_NUMBER INTO v_id FROM USER_SEQUENCES WHERE UPPER(SEQUENCE_NAME) = UPPER('DNPTXLINKSKINDSNOBCH_DBT_SEQ');
    IF :NEW.T_ID >= v_id THEN
      RAISE DUP_VAL_ON_INDEX;
    END IF;
  END IF;
END;
/
