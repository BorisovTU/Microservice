COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_TYPESNOB_OLD IS 'Прежний вид СНОБ'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_TAXBASETYPE_OLD IS 'Прежний вид НОБ для Хранилища'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_BEGDATETYPESNOB_OLD IS 'Прежняя дата начала действия'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_ENDDATETYPESNOB_OLD IS 'Прежняя дата окончания действия'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_TYPESNOB_NEW IS 'Новый вид СНОБ'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_TAXBASETYPE_NEW IS 'Новый вид НОБ для Хранилища'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_BEGDATETYPESNOB_NEW IS 'Новая дата начала действия'/
COMMENT ON COLUMN DNPTXLINKSKINDSNOBCH_DBT.T_ENDDATETYPESNOB_NEW IS 'Новая дата окончания действия'/
