                           attrGroup    IN DOBJATCOR_DBT.T_GROUPID%TYPE,
                           NumInList    IN DOBJATTR_DBT.T_NUMINLIST%TYPE,
                           fromDate     IN DOBJATCOR_DBT.T_VALIDFROMDATE%TYPE,
                           objectCode   IN DSFCONTR_DBT.T_NUMBER%TYPE)
    IS
        v_Count   NUMBER := 0;
    BEGIN
        SELECT COUNT (1)
          INTO v_Count
          FROM DOBJATCOR_DBT
         WHERE     T_OBJECT =
                   LPAD (
                       (SELECT dlcontr.T_DLCONTRID
                          FROM DDLCONTR_DBT dlcontr, DSFCONTR_DBT sfcontr
                         WHERE     TRIM (sfcontr.t_NUMBER) = objectCode
                               AND dlcontr.T_SFCONTRID = sfcontr.T_ID),
                       34,
                       '0')
               AND T_OBJECTTYPE = 207
               AND T_GROUPID = 199;

        IF v_Count = 0
        THEN
            INSERT INTO DOBJATCOR_DBT (T_OBJECTTYPE,
                                       T_GROUPID,
                                       T_ATTRID,
                                       T_OBJECT,
                                       T_GENERAL,
                                       T_VALIDFROMDATE,
                                       T_OPER,
                                       T_VALIDTODATE,
                                       T_SYSDATE,
                                       T_SYSTIME,
                                       T_ISAUTO,
                                       T_ID)
                SELECT objType,
                       attrGroup,
                       Attr.t_AttrID,
                       LPAD (
                           (SELECT dlcontr.T_DLCONTRID
                              FROM DDLCONTR_DBT dlcontr, DSFCONTR_DBT sfcontr
                             WHERE     TRIM (sfcontr.t_NUMBER) = objectCode
                                   AND dlcontr.T_SFCONTRID = sfcontr.T_ID),
                           34,
                           '0'),
                       'X',
                       fromDate,
                       9997,
                       TO_DATE ('31.12.9999', 'DD.MM.YYYY'),
                       TRUNC (SYSDATE),
                       TO_DATE (
                           '01.01.0001 ' || TO_CHAR (SYSDATE, 'HH24:MI:SS'),
                           'DD.MM.YYYY HH24:MI:SS'),
                       'X',
                       0
                  FROM dobjattr_dbt Attr
                 WHERE     Attr.T_NUMINLIST = NumInList
                       AND Attr.t_ObjectType = objType
                       AND Attr.t_GroupID = attrGroup;
        
        END IF;
    EXCEPTION 
    WHEN OTHERS THEN NULL;
    END;
