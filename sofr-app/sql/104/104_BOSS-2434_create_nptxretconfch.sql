    T_ID NUMBER(10)
  , T_RCID NUMBER(10)
  , T_TAXPERIOD_OLD NUMBER(5)
  , T_DATEPART1_OLD DATE
  , T_DATEPART2_OLD DATE
  , T_TAXPERIOD_NEW NUMBER(5)
  , T_DATEPART1_NEW DATE
  , T_DATEPART2_NEW DATE
  , T_OPER NUMBER(5)
  , T_RECDATE DATE
)
/
