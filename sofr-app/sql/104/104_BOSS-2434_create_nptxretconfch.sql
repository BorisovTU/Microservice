COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_ID IS 'Идентификатор записи'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_RCID IS 'Идентификатор записи из настроечной таблицы'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_TAXPERIOD_OLD IS 'Прежний налоговый период'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_DATEPART1_OLD IS 'Прежняя дата отсчета для раздела 1'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_DATEPART2_OLD IS 'Прежняя дата отсчета для раздела 2'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_TAXPERIOD_NEW IS 'Новый налоговый период'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_DATEPART1_NEW IS 'Новая дата отсчета для раздела 1'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_DATEPART2_NEW IS 'Новая дата отсчета для раздела 2'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_OPER IS 'Операционист, изменивший запись'/
COMMENT ON COLUMN DNPTXRETCONFCH_DBT.T_RECDATE IS 'Дата изменения записи'/

